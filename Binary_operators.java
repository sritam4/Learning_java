import java.util.*;
public class Binary_operators {
    public static void main(String args[]){
        //bit manipulation operators
        System.out.println("5 & 6 ="+ (5 & 6));
        System.out.println("5 | 6 ="+ (5 | 6));
        System.out.println("5 ^ 6 ="+ (5^6));
        System.out.println("~5 ="+ (~5));
        System.out.println("~6 ="+ (~8));
        System.out.println("5<<2 ="+ (5<<2));
        System.out.println("5>>2 ="+ (5>>2));
    }
}